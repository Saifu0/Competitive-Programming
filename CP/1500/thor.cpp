const int mxn = 3e5+10;
vi g[mxn];
multiset<int>s;

int32_t main(){
	NINJA;

	int n,q; cin >> n >> q;
	int cur = 0;
	while(q--){
		int t; cin >> t;
		if(t==1){
			int x; cin >> x;
			cur++;
			g[x].pb(cur);
			s.insert(cur);
		}else if(t==2){
			int x; cin >> x;
			for(auto &it: g[x]){
				if(s.find(it)!=s.end()) s.erase(it);
			}
			g[x].clear();
		}else{
			int x; cin >> x;
			while(sz(s) && x >= *s.begin()) s.erase(s.begin());
		}
		cout << sz(s) << endl;
	}
	
	return 0;
}

